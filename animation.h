// Fungsi Render
void render_anim(void) {
    // Menulis seluruh layar sekaligus (Raw byte)
    oled_write_raw_P((const char*)anim_frames[anim_frame], ANIM_SIZE);
}

// Fungsi Update Frame
void anim_timer_task(void) {
    if (timer_elapsed32(anim_timer) >= pgm_read_word(&anim_delays[anim_frame])) {
        anim_timer = timer_read32();
        anim_frame = (anim_frame + 1) % ANIM_FRAME_COUNT;
    }
}

// Fungsi Inisialisasi
void anim_init(void) {
    anim_frame = 0;
    anim_timer = timer_read32();
}

